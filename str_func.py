def upper_str(string: str) -> str:
    """
    делаем все буквы в строке заглавными
    :param string:
    :return string:
    """
    return string.upper()


def title_str(string: str) -> str:
    """
    делаем каждую букву в строке заглавной
    :param string:
    :return string:
    """
    return string.title()

